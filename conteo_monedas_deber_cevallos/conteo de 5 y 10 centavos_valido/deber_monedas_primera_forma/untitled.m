% --- Executes on button press in btn_contar.
function btn_contar_Callback(hObject, eventdata, handles)
global coin2;global a;
%% Label and count connected components
[L Ne]=bwlabel(double(coin2));
%% Measure properties of image regions
prop=regionprops(L,'Area','Centroid');
%% Counter
total=0; contador1=0;  contador2=0; contador3=0;
%% Show image
imshow(imread('coins.png'));
%% Money count based on area of coin
for n=1:size(prop,1) %For 1 to Total number of coins
    cent=prop(n).Centroid;
    X=cent(1);Y=cent(2);
    if prop(n).Area>2000
        text(X-10,Y,'5 C') 
        total=total+5;
        contador1=contador1+1;
    else
        total=total+10;
        text(X-10,Y,'10 C') 
        contador2=contador2+1;
    end
    contador3=contador1+contador2;
end
hold on
mon=strcat(num2str(total),' Centavos');
aux = strcat(num2str(contador1));
aux2 = strcat(num2str(contador2));
aux3 = strcat(num2str(contador3));
set(handles.edit1,'String',aux);
set(handles.edit2,'String',aux2);
set(handles.edit3,'String',mon);
set(handles.edit4,'String',aux3);

